print("Welcome to the treasure island game")

Direction = input("You're at a cross road. Where do you want to go? Type 'left' or 'right'\n").lower()
if Direction != 'left':
    print("Game Over!")
    exit()
Swim_or_wait = input("You come to a lake. There is an island in the middle of the lake. Type 'wait' to wait for a boat. Type 'swim' to swim across.\n").lower()
if Swim_or_wait != 'wait':
    print("Game Over!")
    exit()
Door = input("You arrive at the island unharmed. There is a house with 3 doors. One red, one yellow and one blue. Which colour do you choose?\n").lower()
if Door != 'yellow':
    print("Game Over!")
    exit()

win_the_game = Direction == 'left' and Swim_or_wait == 'wait' and Door == 'yellow'

if win_the_game:
    print("You found the treasure! You Win!")
else:
    print("Game Over!")